    public void addNewUploadForm(SavedIPInfo sip) {
        if(!MainWindow.mw.isServerRunning()) {
            return;
        }

        if(sip == null) {
            sip = new SavedIPInfo();
            sip.setIp(UsefulMethods.LOCAL_HOST_IP);
            sip.setPort(UsefulMethods.getPortNumber());
        }

        UploadFormPanel ufp = new UploadFormPanel(sip);
        String tabName = "New";
        if(!(sip.getIp().equals(UsefulMethods.LOCAL_HOST_IP) && (sip.getName() == null)))
            tabName = UsefulMethods.getShortNameOfIP(sip.getIp());

        this.jTabbedPane1.addTab(tabName, ufp);
        this.jTabbedPane1.setSelectedComponent(ufp);

        showDialog();
    }

    public void removePanel(JPanel p) {
        jTabbedPane1.remove(p);
        closingLogic();
    }

    public void focusOnPanel(JPanel p) {
        jTabbedPane1.setSelectedComponent(p);
    }

    private void closingLogic() {
        if(jTabbedPane1.getTabCount() == 0)
            closeDialog();
    }

    public void showDialog() {
        if(!this.isVisible())
            this.setVisible(true);
        this.pack();
    }

    public void closeDialog() {
        if(this.isVisible())
            this.setVisible(false);
    }
